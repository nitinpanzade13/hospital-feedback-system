def get_form_config(department, language=None):
    """Get form configuration for a department, optionally for a language."""
    config = DEPARTMENT_FORM_CONFIG.get(department, {})
    if language:
        return config.get("languages", {}).get(language, {})
    return config

def get_form_id(department, language=None):
    """Get form ID for a department and language."""
    config = get_form_config(department, language)
    return config.get("form_id")

def get_form_backend_url(department, language=None):
    """Get Apps Script backend URL for a department and language."""
    config = get_form_config(department, language)
    return config.get("backend_url")
